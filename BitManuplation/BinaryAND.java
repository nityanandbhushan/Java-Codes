package BitManuplation;

public class BinaryAND { 
    public static void main(String args[]){  
        ///Binary AND &
        System.out.println(5&6); 
        //Binary OR |
        System.out.println(5|6); 
        //Binay XOR ^ 
        System.out.println(5^6);
        //Binary One's Compliment 
        System.out.println(~5);
        //Binary Left Shift 
        System.out.println(5<<1); 
        //Binary Right Shift 
        System.out.println(5>>1); 

    }
    
}
